/**
 * Change (or not) rider's direction.
 *
 * @author Thomas Duchatelle (tomdush@gmail.com)
 *
 */
public enum DIRECTION_CHANGE {

	/** Keep rider's direction */
	KEEP,

	/** Inverse riders's direction */
	CHANGE,

	/** Rider's can change direction */
	NEITHER;

}
